package com.example.per6.Lolstory;

public class ReforgedRuneDto {

    private String runePathName, name, shortDesc, longDesc, icon, key;
    private int runePathId, id;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getRunePathName() {
        return runePathName;
    }

    public void setRunePathName(String runePathName) {
        this.runePathName = runePathName;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getShortDesc() {
        return shortDesc;
    }

    public void setShortDesc(String shortDesc) {
        this.shortDesc = shortDesc;
    }

    public String getLongDesc() {
        return longDesc;
    }

    public void setLongDesc(String longDesc) {
        this.longDesc = longDesc;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public int getRunePathId() {
        return runePathId;
    }

    public void setRunePathId(int runePathId) {
        this.runePathId = runePathId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
